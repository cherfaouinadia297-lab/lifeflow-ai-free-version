// دوال الـ IntlCore الموحدة لمنع التخريب والخلل البرمجي
export function formatPercent(value: number, locale: string): string {
  return new Intl.NumberFormat(locale, { style: "percent" }).format(value);
}

export function formatNumber(value: number | string, locale: string): string {
  return new Intl.NumberFormat(locale).format(Number(value));
}

export function formatDateTime(dateString: string, locale: string): string {
  return new Intl.DateTimeFormat(locale, {
    year: "numeric", month: "short", day: "numeric",
    hour: "2-digit", minute: "2-digit", second: "2-digit"
  }).format(new Date(dateString));
}
