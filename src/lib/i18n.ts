// All UI strings live here as flat dot-keys. Helpers expose Intl-aware
// date/number/percent formatting so screens never call toLocaleString themselves.

export interface LangMeta {
  code: string;
  label: string;
  dir: "ltr" | "rtl";
  locale: string;
  country: string; // ISO-3166-1 alpha-2 for news/holidays
  newsLang: string;
}

