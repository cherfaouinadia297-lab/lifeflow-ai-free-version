function interpolate(s: string, vars?: Record<string, string | number>): string {
  if (!vars) return s;
  return s.replace(/\{(\w+)\}/g, (_, k) =>
    vars[k] !== undefined ? String(vars[k]) : `{${k}}`,
  );
}

/** Backwards-compatible: t(lang, key, vars?). */
export function t(lang: string, key: string, vars?: Record<string, string | number>): string {
  const dict = STRINGS[lang] ?? STRINGS.en;
  const resolved = LEGACY_ALIASES[key] ?? key;
  const value = dict[resolved] ?? dict[key] ?? STRINGS.en[resolved] ?? STRINGS.en[key] ?? key;
  return interpolate(value, vars);
}

/** React-friendly bundle of helpers tied to a language. */
export function makeI18n(langCode: string) {
  const meta = getLangMeta(langCode);
  const locale = meta.locale;

  return {
    lang: meta.code,
    dir: meta.dir,
    locale,
    meta,
    t: (key: string, vars?: Record<string, string | number>) => t(meta.code, key, vars),
    formatDate: (date: Date | string | number, opts?: Intl.DateTimeFormatOptions) =>
      new Intl.DateTimeFormat(locale, {
        weekday: "long",
        day: "numeric",
        month: "long",
        year: "numeric",
        ...opts,
      }).format(new Date(date)),
    formatTime: (date: Date | string | number) =>
      new Intl.DateTimeFormat(locale, { hour: "2-digit", minute: "2-digit" }).format(new Date(date)),
    formatNumber: (n: number, opts?: Intl.NumberFormatOptions) =>
      new Intl.NumberFormat(locale, opts).format(n),
    formatPercent: (fraction: number) =>
      new Intl.NumberFormat(locale, { style: "percent", maximumFractionDigits: 0 }).format(fraction),
    relativeDays: (date: Date | string) => {
      const d = new Date(date);
      const today = new Date();
      today.setHours(0, 0, 0, 0);
      d.setHours(0, 0, 0, 0);
      const days = Math.round((d.getTime() - today.getTime()) / 86_400_000);
      const rtf = new Intl.RelativeTimeFormat(locale, { numeric: "auto" });
      return rtf.format(days, "day");
    },
  };
}

export type I18n = ReturnType<typeof makeI18n>;
